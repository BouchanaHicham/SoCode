
# Membership Operators
# In
result = "a" in "apple"
print("In:", result)  # Output: True

# Not In
result = "b" not in "apple"
print("Not In:", result)  # Output: True

# Identity Operators
# Is
result = (5 is 5)
print("Is:", result)  # Output: True

# Is Not
result = (5 is not 3)
print("Is Not:", result)  # Output: True

# Conditional Expression
result = "Yes" if 5 > 3 else "No"
print("Conditional Expression:", result)  # Output: Yes
