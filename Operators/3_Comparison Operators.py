
# Comparison Operators
# Equal to
result = (5 == 3)
print("Equal to:", result)  # Output: False

# Not equal to
result = (5 != 3)
print("Not equal to:", result)  # Output: True

# Greater than
result = (5 > 3)
print("Greater than:", result)  # Output: True

# Less than
result = (5 < 3)
print("Less than:", result)  # Output: False

# Greater than or equal to
result = (5 >= 3)
print("Greater than or equal to:", result)  # Output: True

# Less than or equal to
result = (5 <= 3)
print("Less than or equal to:", result)  # Output: False