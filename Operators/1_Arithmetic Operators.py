
# Arithmetic Operators
# Addition
result = 5 + 3
print("Addition:", result)  # Output: 8

# Subtraction
result = 5 - 3
print("Subtraction:", result)  # Output: 2

# Multiplication
result = 5 * 3
print("Multiplication:", result)  # Output: 15

# Division
result = 5 / 2
print("Division:", result)  # Output: 2.5

# Floor Division
result = 5 // 2
print("Floor Division:", result)  # Output: 2

# Modulus
result = 5 % 2
print("Modulus:", result)  # Output: 1

# Exponentiation
result = 5 ** 2
print("Exponentiation:", result)  # Output: 25

