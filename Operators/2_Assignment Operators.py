
# Assignment Operators
# Simple Assignment
x = 5
print("Simple Assignment:", x)  # Output: 5

# Add and Assign
x += 3
print("Add and Assign:", x)  # Output: 8

# Subtract and Assign
x -= 3
print("Subtract and Assign:", x)  # Output: 5

# Multiply and Assign
x *= 3
print("Multiply and Assign:", x)  # Output: 15

# Divide and Assign
x /= 3
print("Divide and Assign:", x)  # Output: 5.0

# Floor Divide and Assign
x //= 3
print("Floor Divide and Assign:", x)  # Output: 1

# Modulus and Assign
x %= 3
print("Modulus and Assign:", x)  # Output: 1

# Exponentiate and Assign
x **= 2
print("Exponentiate and Assign:", x)  # Output: 1