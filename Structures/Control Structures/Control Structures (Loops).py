# ------------------- FOR IN ------------------- 
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
# ------------------- WHILE -------------------
counter = 0
while counter < 5:
    print(counter)
    counter += 1
# ------------------- += and -= Operators -------------------

# Initial value
counter = 10

# Using += operator
counter += 5  # Equivalent to counter = counter + 5

# Now counter is 15
print(counter)  # Output: 15

# --------------------------------

# Initial value
counter = 10

# Using -= operator
counter -= 3  # Equivalent to counter = counter - 3

# Now counter is 7
print(counter)  # Output: 7



